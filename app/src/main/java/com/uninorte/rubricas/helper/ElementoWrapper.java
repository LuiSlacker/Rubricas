package com.uninorte.rubricas.helper;


public class ElementoWrapper {

    private int nivel;
    private int elementoId;
    private String elementoNombre;

    public ElementoWrapper() {
    }

    public int getNivel() {
        return nivel;
    }

    public void setNivel(int nivel) {
        this.nivel = nivel;
    }

    public int getElementoId() {
        return elementoId;
    }

    public void setElementoId(int elementoId) {
        this.elementoId = elementoId;
    }

    public String getElementoNombre() {
        return elementoNombre;
    }

    public void setElementoNombre(String elementoNombre) {
        this.elementoNombre = elementoNombre;
    }

}
